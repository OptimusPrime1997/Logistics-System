    private String calSum(){
    	double sum = 0;
    	for(int i = 0;i < dataVector.size();i++){
    		String value = (String)jTable.getValueAt(i, 3);
    		if(value!=null)
    			sum += Double.parseDouble(value);
    	}
    	return sum+"";
    }

